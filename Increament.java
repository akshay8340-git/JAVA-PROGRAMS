class Increament
{
	public static void main(String[] args) 
	{
		
		int a = 100;
		
		
		System.out.println(++a); //101 valued added by 1 firstly then stored
		System.out.println(++a); // 102 valued added by 1 firstly then stored
		System.out.println(a++); // 102 first print then add 1 value so real value is now 103
		System.out.println(++a); //104 value added by 1 then stored value is now 104
		System.out.println(a++); // 104 first print value then add 1 so new value is 105
		System.out.println(++a); //106 add 1 value and store 
		
	}
}
